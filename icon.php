function klb_icon_element_fonts_enqueue( $font ) {
  switch ( $font ) {
    case 'elegant':
    wp_enqueue_style( 'eleganticons' );
	break;
    case 'linea':
    wp_enqueue_style( 'lineaicons' );
	break;
    case 'etline':
    wp_enqueue_style( 'lineaicons' );
    break;
    case 'flat':
    wp_enqueue_style( 'flaticons' );
  }
}
